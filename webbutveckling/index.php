<h4 id="jquery">jQuery</h4>

<p><a href="https://jquery.com/">https://jquery.com/</a></p>

<p>Vilken version av jQuery ska jag använda? </p>

<ul>
<li>Använd version 2.x om du vet att ingen besökare använder Internet Explorer 8 eller tidigare</li>
</ul>

<p>Det finns två sätt att komma igång med jQuery:</p>

<ul>
<li><a href="https://jquery.com/download/#downloading-jquery">Ladda ned</a> jQuery (<em>compressed production jQuery</em>) och inkludera filen med: <br>
<code>&lt;script src="jquery-2.2.0.min.js"&gt;&lt;/script&gt;</code></li>
<li>Inkludera direkt från en annan server, till exempel <a href="https://developers.google.com/speed/libraries/#jquery">från Google</a>:  <br>
<code>&lt;script src="https://ajax.googleapis.com/ajax/libs/jquery/2.2.0/jquery.min.js"&gt;&lt;/script&gt;</code> </li>
</ul>

<p>Läs mer om jQuery och hur man använder det i <a href="https://sjk15.slack.com/files/oscar/F0MT6STFW/jquery.pdf">PDF-filen på Slack</a>.</p>



