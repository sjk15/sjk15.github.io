<h4 id="projecttable">ProjectTable</h4>

<p>ProjectTable använder sig av </p>

<ul>
<li><em>MVC</em> för uppdelning av modell från View och Control  <br>
<ul><li>En modell för ett spel, <code>GameModel</code>, kommer att hålla  <code>MyTableModel</code> uppdaterad som i sin tur uppdaterar en <code>JTable</code></li></ul></li>
<li>Designmönstret <em>Observable</em> / <em>observer</em> för att <code>MyTableModel</code> ska observera <code>GameModel</code> och uppdatera en <code>JTable</code> därefter</li>
<li>Designmönstret <em>Singleton</em> för att underlätta åtkomst till <code>GameModel</code> på olika ställen i projektet (tänk “global variabel”) </li>
<li><em>JTable</em> och en egengjord <code>MyTableModel</code> som förser JTable med data. Denna data hämtas ifrån <code>GameModel</code></li>
<li><em>javadoc</em> för att skapa dokumentation</li>
</ul>

<p>Projektet består av <a href="https://www.dropbox.com/sh/f678zxz9x3d0u02/AACYHrTmx_xp9naJ9jk3EDnFa?dl=0">fyra filer att ladda ned</a> samt <a href="http://edu.oscarb.se/SJK15/OOP-och-Java/project-table-docs/">genererad dokumentation</a>.  </p>



