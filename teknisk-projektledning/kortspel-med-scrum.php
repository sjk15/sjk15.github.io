<body><div class="container"><h1 id="kortspel-med-scrum">Kortspel med SCRUM</h1>

<p>Uppgiften går ut på att ni i grupp jobbar med SCRUM som projektstyrningsmetod i två veckor och tar fram ett eller flera kortspel. </p>

<h2 id="utförande">Utförande</h2>

<p>Innan kursens början har ni utfört en förberedande uppgift där ni delat in er i grupper och undersökt olika kortspel genom att ta fram UML- och flödesdiagram samt pseudo-kod. Kortspelen har sedan rangordnats efter svårighetsgrad att implementera i kod.</p>

<p>De undersökta kortspelen blir del av er <em>project backlog</em>. Baserat på denna ska ni:</p>

<ol>
<li>Planera en release av ett program för terminalen/kommandotolken där man kan spela ett kortspel</li>
<li>Sätt upp en <em>release backlog</em> för ett av kortspelen, förslagsvis börjar ni med det enklaste att implementera.</li>
<li>Utse en person i gruppen som SCRUM-mästare. Detta kan ni t.ex. slumpa fram med Java.</li>
<li>Från er <em>release backlog</em> tar ni fram en <em>sprint</em>. Använd <a href="https://trello.com/">Trello</a> till er hjälp. Varje <em>sprint</em> ska ha en egen <em>board</em> i Trello.</li>
<li>Genomför daglia SCRUM-möten där ni tar reda på vad ni gjort sen senast, vad ni ska göra till nästa gång samt eventuella hinder.</li>
<li>Ta fram en <em>burndown chart</em> och håll den uppdaterad för att se hur arbetet går.</li>
<li>När ett kortspel är klart (en <em>release</em> är klar) upprepar ni från steg 2. </li>
</ol>

<h2 id="redovisning">Redovisning</h2>

<ul>
<li>Skapa en privat grupp i Slack där ni även bjudit in utbildaren. </li>
<li>Se till att det i denna finns skrivet eller länkar till: <br>
<ul><li><em>Product backlog</em></li>
<li><em>Release backlog</em></li>
<li><em>Burndown chart</em></li>
<li>En <em>board</em> på Trello per sprint</li>
<li>En sammanfattning från era dagliga SCRUM-möten</li></ul></li>
<li>De färdiga kortspelen ska ni ladda upp på GitHub och länka till i er privata grupp på Slack.</li>
</ul>

<p>Förslagsvis jobbar ni med GitHub under hela processen.</p>

