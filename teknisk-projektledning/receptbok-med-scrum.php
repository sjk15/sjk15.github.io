<h2 id="utförande">Utförande</h2>

<p>Innan kursens början har ni utfört en förberedande uppgift där ni individuellt tagit fram en hemsida om ett recept. Dessa recept samlas nu ihop till en receptbok på internet. Förutom recepten finns det ett antal ytterligare önskemål från produktägaren. Utgå ifrån produktbackloggen nedan från produktägaren.</p>

<h3 id="produktbacklogg-från-produktägaren">Produktbacklogg från produktägaren</h3>

<p>Webbplatsen “Receptboken” ska ha…</p>

<ul>
<li>En enhetlig design</li>
<li>En startsida som presenterar recepten</li>
<li>Minst lika många recept som medlemmar i SCRUM-teamet</li>
<li>En sida med generella tips och råd vid bakning/matlagning</li>
<li>En topplista över recepten sorterad efter receptens betyg</li>
<li>Ett slumpässigt recept som presenteras som “dagens förslag”</li>
<li>En sida där recepten är indelade i kategorier</li>
<li>Påhittad kontaktinformation</li>
</ul>

<h3 id="scrum-teamet">SCRUM-teamet</h3>



<h4 id="dag-1">Dag 1</h4>

<ol>
<li>Dela in er i SCRUM-team om minst 3 och högst 5 personer. Hitta på ett namn på teamet.</li>
<li>Skapa en privat grupp i Slack med utbildaren inbjuden.</li>
<li>Utse en i gruppen som SCRUM-mästare.</li>
<li>Skapa ett team på Trello med utbildaren inbjuden.</li>
<li>Skapa ett Board på Trello med listorna <em>Product backlog</em>, <em>Sprint backlog/To-Do</em>, <em>Doing/In progress</em> och <em>Done</em>.</li>
<li>Utse en gruppmedlems GitHub-konto vilket ni versionshanterar mot</li>
<li>Utifrån produktbackloggen planerar ni en sprintbacklogg. <br>
<ul><li>För varje post i backloggen ska <em>definition of done</em> bestämmas</li></ul></li>
<li>Påbörja en Burndown Chart</li>
<li>Skissa fram med papper och penna eller datorprogram struktur för webbplatsen och webbplatsens design</li>
</ol>



<h4 id="dag-2">Dag 2–</h4>

<ol>
<li>Genomför ett dagligt SCRUM-möte där ni tar reda på vad ni gjort sen senast, vad ni ska göra tills nästa gång samt eventuella hinder. Mötet, eller en sammafattning av detta, ska finnas i er kanal på Slack.</li>
<li>Arbeta med det som finns att göra i sprintbackloggen</li>
<li>Uppdatera Trello och teamets Burndown Chart</li>
<li>Rotera rollen som SCRUM-mästare</li>
</ol>



<h2 id="redovisning">Redovisning</h2>

<p>Redovisnignen är kontinuerlig och består av projektet på GitHub samt individuell aktivitet på GitHub, Slack och Trello.</p>

<p>I er kanal på Slack ska det finnas länkar till:</p>

<ul>
<li>sprintbackloggen på Trello</li>
<li>Burndown Chart</li>
<li>projektet på GitHub och GitHub Pages</li>
</ul>

<p>Utöver detta ska även de dagliga mötena finnas sammanfattade på Slack.</p>

<p>Deadline för sprinten är <strong>söndag 13 mars kl 23:59</strong></p>



